package com.chinasoft.pojo;

public class User {

	private String yno;
	private String pwd;
	private String yname;
	public String getId() {
		return yno;
	}
	public void setId(String yno) {
		this.yno = yno;
	}
	public String getName() {
		return yname;
	}
	public void setName(String yname) {
		this.yname = yname;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	
	
	
}
